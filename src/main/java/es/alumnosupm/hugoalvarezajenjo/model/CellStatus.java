package es.alumnosupm.hugoalvarezajenjo.model;

/**
 * The `CellStatus` enum represents the possible statuses of a cell in a Minesweeper game.
 * - `REVEALED`: The cell has been revealed.
 * - `MARKED`: The cell has been marked by the player.
 * - `NONE`: The cell has no special status.
 *
 * @author Hugo Alvarez Ajenjo
 * @version 0.0
 */
public enum CellStatus {
    REVEALED, MARKED, NONE
}
