package es.alumnosupm.hugoalvarezajenjo.view;

/**
 * The `SelectionMode` enum represents the two possible modes for player selection in a Minesweeper game.
 * - `FLAG_MODE`: The player is in flag mode, used to mark potential mine locations.
 * - `DIG_MODE`: The player is in dig mode, used to reveal cell contents.
 *
 * @author Hugo Alvarez Ajenjo
 * @version 1.0
 */
public enum SelectionMode {
    FLAG_MODE, DIG_MODE
}
